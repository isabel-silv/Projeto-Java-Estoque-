    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    Produto pro = new Produto();
ProdutoDAO dao = new ProdutoDAO();
boolean status;
boolean resposta; 

 String codigoTexto = txtCodigo.getText();
    if (codigoTexto.isEmpty()) {
        JOptionPane.showMessageDialog(null, "C처digo n찾o pode estar vazio");
        return;
    }

    try {
        int codigo = Integer.parseInt(codigoTexto);
        pro.setId(codigo);
        
        
        String nomeProduto = txtNome.getText();
    if (nomeProduto.isEmpty()) {
    JOptionPane.showMessageDialog(null,"O campo Nome n찾o pode estar vazio.");
    return;
}
        pro.setNome(txtNome.getText());

        String valorTexto = txtValor.getText();
        if (!valorTexto.isEmpty()) {
            double valor = Double.parseDouble(valorTexto);
            pro.setValor(valor);
        } else {
            JOptionPane.showMessageDialog(null, "Valor n찾o pode estar vazio");
            return;
        }

        String quantidadeTexto = txtQuantidade.getText();
        if (!quantidadeTexto.isEmpty()) {
            int quantidade = Integer.parseInt(quantidadeTexto);
            pro.setQuantidadeEstoque(quantidade);
        } else {
            JOptionPane.showMessageDialog(null, "Quantidade n찾o pode estar vazia");
            return;
        }

           try {
            String dataRecebida = txtData.getText();
            SimpleDateFormat conversor = new SimpleDateFormat("dd/MM/yyyy");
            Date dataConvertida = conversor.parse(dataRecebida);
            java.sql.Date sqlDate = new java.sql.Date(dataConvertida.getTime());
            pro.setData(sqlDate);
        } catch (ParseException pe) {
            JOptionPane.showMessageDialog(null, "Insira uma data v찼lida no formato dd/MM/yyyy.");
            return; 
        }
        
        status = dao.conectar();

        if (!status) {
            JOptionPane.showMessageDialog(null, "Erro de conex찾o");
        } else {
            resposta = dao.salvar(pro);

            if (resposta) {
                JOptionPane.showMessageDialog(null, "Dados inclu챠dos com sucesso");
                limpaTela();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar inserir dados");
            }
        }
        dao.desconectar();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Verifique os campos e insira valores v찼lidos.");
    }


    }//GEN-LAST:event_btnSalvarActionPerformed

